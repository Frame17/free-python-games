def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    global pacman
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)

    pac_x, pac_y = random_init()
    pacman = vector(pac_x, pac_y)

    can_x, can_y = random_init()
    while can_x == pac_x and can_y == pac_y:        # reinit in case of collision
        can_x, can_y = random_init()
    path.up()
    path.goto(can_x + 10, can_y + 10)
    path.dot(5, 'white')


def move():
    "Move pacman"
    writer.undo()

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    update()

    ontimer(move, 100)


def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


def random_init():
    candy_pos = random.randint(0, len(tiles) + 1)
    if tiles[candy_pos] == 0:
        while tiles[candy_pos] != 1:
            candy_pos = (candy_pos + 1) % len(tiles)

    # complex high-level mathematics
    x = (candy_pos % 20) * 20 - 200
    y = 180 - (candy_pos // 20) * 20
    return x, y


